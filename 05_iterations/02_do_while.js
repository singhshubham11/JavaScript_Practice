// While

let i = 0;
while (i <= 10) {
    // console.log(`value of index is ${i}`);
    i = i + 2;;
}

let myArray = ["karan", "raj", "lalith", "heman"];
let arr = 0;
while (arr < myArray.length) {
    // console.log(`value is ${myArray[arr]}`);
    arr++;
}

// +++++++++ Do While ++++++++++
let score = 1;
do {
    console.log(`score is ${score}`);
    score++;
} while (score <= 10);